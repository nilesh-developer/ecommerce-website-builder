import mongoose from "mongoose";

const ContactFormSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
})

export const contactforms = mongoose.model("contactforms", ContactFormSchema)